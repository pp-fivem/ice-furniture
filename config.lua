function GetFurnitureData(name)
    local found = {}
    for id, data in pairs(Config.Furniture) do
        for index, v in pairs(data.furniture) do
            if v.object == name or v.label == name then
                found = v
                found.id = id
                found.index = index
                found.category = data.label
                break
            end
        end
    end
    return found
end exports('GetFurnitureData', GetFurnitureData)

